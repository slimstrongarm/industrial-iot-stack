    "software_stack": {
        "python_version": "3.11",
        "node_version": "18.x",
        "node_red_version": "latest",
        "discovery_agent": "enhanced",
        "health_monitoring": "enabled"
    }
}
EOF
    
    echo "$compressed_image"
}

